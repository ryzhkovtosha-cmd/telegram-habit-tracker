"""
Главный модуль Telegram-бота.
Создаёт объект бота, регистрирует все обработчики команд и callback-ов,
запускает планировщик напоминаний и переходит в режим бесконечного опроса (polling).
"""

import telebot
from config import BOT_TOKEN            # токен бота из конфигурации
from handlers import register_handlers   # функция регистрации всех обработчиков
from scheduler import start_scheduler    # функция запуска планировщика

def main():
    """
    Основная функция запуска бота.
    """
    # Создаём экземпляр бота с токеном из переменных окружения
    bot = telebot.TeleBot(BOT_TOKEN)

    # Регистрируем все обработчики (команды и callback-запросы)
    register_handlers(bot)

    # Запускаем фоновый планировщик для ежедневных напоминаний
    start_scheduler(bot)

    print("Бот запущен и готов к работе...")
    # Запускаем бесконечный цикл получения обновлений от Telegram
    # Этот вызов блокирует выполнение, поэтому он должен быть последним.
    bot.infinity_polling()

if __name__ == "__main__":
    main()